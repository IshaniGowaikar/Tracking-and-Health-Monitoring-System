package com.Bean;

public class SoldierBean 
{
	private int soldier_Id;
	private String soldier_Name;
	private String soldier_BloodGroup;
	private String soldier_MobileNo;
	
	
	public String getSoldier_MobileNo() {
		return soldier_MobileNo;
	}
	public void setSoldier_MobileNo(String soldier_MobileNo) {
		this.soldier_MobileNo = soldier_MobileNo;
	}
	public int getSoldier_Id() {
		return soldier_Id;
	}
	public void setSoldier_Id(int soldier_Id) {
		this.soldier_Id = soldier_Id;
	}
	public String getSoldier_Name() {
		return soldier_Name;
	}
	public void setSoldier_Name(String soldier_Name) {
		this.soldier_Name = soldier_Name;
	}
	public String getSoldier_BloodGroup() {
		return soldier_BloodGroup;
	}
	public void setSoldier_BloodGroup(String soldier_BloodGroup) {
		this.soldier_BloodGroup = soldier_BloodGroup;
	}
	
	
}

